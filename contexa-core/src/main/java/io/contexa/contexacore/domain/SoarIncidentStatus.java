package io.contexa.contexacore.domain;

/**
 * SOAR 인시던트의 전체 라이프사이클 상태를 정의하는 열거형.
 * 각 상태는 SOAR 표준 공정의 단계를 나타낸다.
 */
public enum SoarIncidentStatus {

    /**
     * 새로운 보안 이벤트가 접수되어 인시던트가 막 생성된 초기 상태.
     * 아직 어떤 분석이나 처리도 시작되지 않았다.
     */
    NEW,

    /**
     * 플레이북이 시작되어, AI가 이벤트의 심각도, 유형 등을 분석하고
     * 어떤 플레이북을 적용할지 결정하는 '분류(Triage)' 단계가 진행 중인 상태.
     */
    TRIAGE,

    /**
     * AI 에이전트가 감사 로그 조회 등 가용한 도구(Tool)를 사용하여
     * 위협에 대한 추가 정보를 수집하고 컨텍스트를 강화하는 '조사(Investigation)' 단계가 진행 중인 상태.
     */
    INVESTIGATION,

    /**
     * 조사된 내용을 바탕으로, AI가 대응을 위한 실행 계획(실행할 도구 목록 등)을
     * 수립하는 '계획(Planning)' 단계가 진행 중인 상태.
     */
    PLANNING,

    /**
     * AI가 수립한 계획에 관리자의 승인이 필요한 위험한 조치가 포함되어 있어,
     * 관리자의 승인/거부 결정을 기다리고 있는 상태. 워크플로우가 일시 중지된다.
     */
    PENDING_APPROVAL,

    /**
     * 관리자의 승인을 받았거나, 승인이 필요 없는 대응 조치들이
     * SOAR 시스템에 의해 자동으로 실행되고 있는 '실행(Execution)' 단계.
     */
    EXECUTION,

    /**
     * 모든 대응 조치가 완료된 후, AI가 전체 인시던트 처리 과정을 요약하여
     * 최종 보고서를 생성하는 '보고(Reporting)' 단계.
     */
    REPORTING,

    /**
     * 모든 플레이북 단계가 성공적으로 완료되어 인시던트가 정상적으로 종결된 최종 상태.
     */
    COMPLETED,

    /**
     * '분류(Triage)' 단계에서 AI가 위협이 경미하다고 판단하여,
     * 별도의 조치 없이 자동으로 종결시킨 상태.
     */
    AUTO_CLOSED,

    /**
     * 플레이북 실행 중 예외가 발생하거나, 관리자가 대응 계획을 거부하여
     * 비정상적으로 종결된 최종 상태.
     */
    FAILED,

    /**
     * 플레이북이 진행 중이었으나, 관리자가 UI를 통해 수동으로 강제 종결시킨 상태.
     */
    CLOSED_BY_ADMIN
}